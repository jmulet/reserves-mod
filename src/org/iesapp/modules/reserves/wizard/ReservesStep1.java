    private void fillList() {
        listmodel.clear();
                if(jRadioButton1.isSelected())
                {
                    for(int i=0; i<ReservesModule.listEspais.size(); i++)
                    {
                        String s = StringUtils.noNull( ReservesModule.listEspais.get(i).getAula() ) +" " +
                                   StringUtils.noNull( ReservesModule.listEspais.get(i).getDescripcio() );
                        ReservesStep1.this.listmodel.addElement(s);
                    }
                }
                else
                {
                    for(int i=0; i<ReservesModule.listMaterials.size(); i++)
                    {
                        String s = StringUtils.noNull(ReservesModule.listMaterials.get(i).getMaterial())+" "+
                                   StringUtils.noNull(ReservesModule.listMaterials.get(i).getDescripcio())+" "+
                                   StringUtils.noNull(ReservesModule.listMaterials.get(i).getUbicacio());
                        ReservesStep1.this.listmodel.addElement(s);
                    }
                }
    }

