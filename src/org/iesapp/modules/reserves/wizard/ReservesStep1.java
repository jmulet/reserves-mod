    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
       int sel = jList1.getSelectedIndex();
       ReservesWiz.getWizard().setNextFinishButtonEnabled(sel>=0);
       if(sel>=0)
       {
            if(jRadioButton1.isSelected())
            {
                ReservesWiz.wizardMap.put("type",BeanReserves.AULES+"");
                ReservesWiz.wizardMap.put("idRecurs", ReservesModule.listEspais.get(sel).getAula());
              
                String nomAula = StringUtils.noNull(ReservesModule.listEspais.get(sel).getAula())+" "+
                                 StringUtils.noNull(ReservesModule.listEspais.get(sel).getDescripcio());
                ReservesWiz.getWizard().setTitle(ReservesWiz.bundle.getString("nova")+" : "+ nomAula);
                ReservesWiz.wizardMap.put("nomRecurs", nomAula);
            }
            else
            {
                ReservesWiz.wizardMap.put("type",BeanReserves.MATERIAL+"");
                ReservesWiz.wizardMap.put("idRecurs",ReservesModule.listMaterials.get(sel).getId()+"");  
                String nomMaterial = StringUtils.noNull(ReservesModule.listMaterials.get(sel).getMaterial())+" "+
                                     StringUtils.noNull(ReservesModule.listMaterials.get(sel).getDescripcio())+" "+
                                     StringUtils.noNull(ReservesModule.listMaterials.get(sel).getUbicacio());
                        
                ReservesWiz.getWizard().setTitle(ReservesWiz.bundle.getString("nova")+" : "+nomMaterial);
                ReservesWiz.wizardMap.put("nomRecurs", nomMaterial); 
            }
       }
      
    }//GEN-LAST:event_jList1ValueChanged

